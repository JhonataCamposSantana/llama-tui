def write_temp_hermes_config(app, model: ModelConfig, home: Path) -> Path:
    home.mkdir(parents=True, exist_ok=True)
    config_path = home / 'config.yaml'
    toolsets = list(getattr(app.hermes, 'toolsets', []) or ['terminal', 'file', 'todo'])
    context_policy = app.hermes_context_policy(model)
    content = '\n'.join([
        '# Generated by llama-tui Hermes benchmark.',
        'model:',
        f'  default: {json.dumps(app.hermes_model_ref(model))}',
        '  provider: "custom"',
        f'  base_url: {json.dumps(app.hermes_base_url(model))}',
        f'  context_length: {int(context_policy["configured_context_length"] or 0)}',
        f'  max_tokens: {int(getattr(model, "output", 0) or 0)}',
        'terminal:',
        '  backend: "local"',
        '  cwd: "."',
        '  timeout: 180',
        '  lifetime_seconds: 300',
        'platform_toolsets:',
        '  cli: [' + ', '.join(json.dumps(item) for item in toolsets) + ']',
        'agent:',
        f'  max_turns: {int(getattr(app.hermes, "max_turns", 20) or 20)}',
        '  verbose: false',
        'memory:',
        '  memory_enabled: false',
        '  user_profile_enabled: false',
        'skills:',
        '  creation_nudge_interval: 0',
        '',
    ])
    config_path.write_text(content, encoding='utf-8')
    return config_path


def hermes_context_record_fields(app, model: ModelConfig) -> Dict[str, object]:
    policy = app.hermes_context_policy(model)
    return {
        'required_context': int(policy.get('required_context', 0) or 0),
        'configured_context_length': int(policy.get('configured_context_length', 0) or 0),
        'actual_ctx_per_slot': int(policy.get('actual_ctx_per_slot', 0) or 0),
        'experimental_context_override': bool(policy.get('experimental_context_override', False)),
        'context_detail': str(policy.get('context_detail', '') or ''),
    }


def isolated_hermes_env(app, model: ModelConfig, home: Path) -> Dict[str, str]:
    env = os.environ.copy()
    env.update(app.build_hermes_env(model, home, benchmark=True))
    env['HERMES_HOME'] = str(home)
    env['HOME'] = str(home)
    env['XDG_CONFIG_HOME'] = str(home / '.config')
    env['XDG_DATA_HOME'] = str(home / '.local' / 'share')
    env['XDG_STATE_HOME'] = str(home / '.local' / 'state')
    env['OPENAI_BASE_URL'] = app.hermes_base_url(model)
    env['OPENAI_API_KEY'] = 'no-key-required'
    env['HERMES_INFERENCE_PROVIDER'] = 'custom'
    env['HERMES_CLIENT'] = 'llama-tui-benchmark'
    env['HERMES_YOLO_MODE'] = '1'
    env['NO_COLOR'] = '1'
    return env


def hermes_cli_preflight(app, timeout: int = HERMES_PREFLIGHT_TIMEOUT) -> Tuple[bool, str]:
    command_prefix = app.hermes_command_prefix()
    if not command_prefix or not app.command_exists(command_prefix[0]):
        return False, f'Hermes command not found: {getattr(app.hermes, "command", "hermes") or "hermes"}'
    with tempfile.TemporaryDirectory(prefix='llama-tui-hermes-preflight-') as home_raw:
        home = Path(home_raw)
        model = ModelConfig(id='preflight', name='preflight', path='', alias='preflight', port=1)
        config_path = write_temp_hermes_config(app, model, home)
        env = isolated_hermes_env(app, model, home)
        checks = [
            command_prefix + ['--version'],
            command_prefix + ['config', 'path'],
            command_prefix + ['chat', '--help'],
        ]
        details: List[str] = []
        for command in checks:
            try:
                result = subprocess.run(
                    command,
                    stdout=subprocess.PIPE,
                    stderr=subprocess.STDOUT,
                    text=True,
                    timeout=timeout,
                    env=env,
                    check=False,
                )
            except subprocess.TimeoutExpired:
                return False, f'{shlex.join(command)} did not return within {timeout}s'
            except Exception as exc:
                return False, f'{shlex.join(command)} failed: {exc}'
            output = concise_failure(result.stdout, limit=240)
            if result.returncode != 0 and '--version' not in command:
                return False, f'{shlex.join(command)} failed ({result.returncode}): {output}'
            if command[-2:] == ['config', 'path'] and str(config_path) not in result.stdout:
                return False, f'Hermes config path mismatch: expected {config_path}, got {output}'
            if output:
                details.append(output)
        sample = build_hermes_run_command(app, model, home, 'preflight')
        unsupported = [flag for flag in ('--provider', '--base-url') if flag in sample]
        if unsupported:
            return False, f'Hermes command uses unsupported flag(s): {", ".join(unsupported)}'
        return True, 'Hermes CLI ready' + (f' ({details[0]})' if details else '')


def build_hermes_run_command(app, model: ModelConfig, workspace: Path, prompt: str) -> List[str]:
    return app.build_hermes_cli_command(model, workspace, prompt=prompt, benchmark=True)


def run_hermes_task(
    app,
    model: ModelConfig,
    task: WorkflowTask,
    timeout: int = HERMES_TASK_TIMEOUT,
    cancel_token: Optional[CancelToken] = None,
    progress: Optional[Callable[[str], None]] = None,
) -> Dict[str, object]:
    with tempfile.TemporaryDirectory(prefix='llama-tui-hermes-work-') as workspace_raw:
        with tempfile.TemporaryDirectory(prefix='llama-tui-hermes-home-') as home_raw:
            check_cancelled(cancel_token)
            workspace = Path(workspace_raw)
            home = Path(home_raw)
            write_fixture(workspace, task)
            config_path = write_temp_hermes_config(app, model, home)
            context_fields = hermes_context_record_fields(app, model)
            env = isolated_hermes_env(app, model, home)
            command = build_hermes_run_command(app, model, workspace, task.prompt)
            preview = shlex.join(command)
            app.append_log(model.id, f'Hermes headless benchmark command: {preview}')
            app.append_log(model.id, f'Hermes benchmark config: {config_path}')
            if progress:
                emit_benchmark_event(
                    progress,
                    'benchmark_phase',
                    model,
                    'hermes',
                    message=f'headless Hermes command: {preview}',
                    phase='Hermes benchmark (headless)',
                    candidate=task.name,
                    command=preview,
                )
            run = run_process_with_metrics(
                command,
                workspace,
                env,
                timeout,
                app,
                cancel_token=cancel_token,
                no_output_timeout=HERMES_NO_OUTPUT_TIMEOUT,
                idle_output_timeout=HERMES_IDLE_OUTPUT_TIMEOUT,
            )
            all_output = list(run.get('stdout', []) or []) + list(run.get('stderr', []) or [])
            unittest_seen = detected_unittest_command(all_output)
            if run.get('aborted'):
                return {
                    'task': task.name,
                    'command_preview': preview,
                    'ok': False,
                    'tests_ok': False,
                    'status': 'aborted',
                    'exit_code': int(run.get('returncode', -1)),
                    'timed_out': bool(run.get('timed_out')),
                    'no_output_timeout': bool(run.get('no_output_timeout')),
                    'idle_output_timeout': bool(run.get('idle_output_timeout')),
                    'aborted': True,
                    'elapsed': float(run.get('elapsed', 0.0) or 0.0),
                    'first_output': float(run.get('first_output', 0.0) or 0.0),
                    'min_ram_available': int(run.get('min_ram_available', 0) or 0),
                    'min_gpu_memory_free': int(run.get('min_gpu_memory_free', 0) or 0),
                    'stdout_tail': list(run.get('stdout', []) or [])[-12:],
                    'stderr_tail': list(run.get('stderr', []) or [])[-12:],
                    'json_event_tail': list(run.get('json_event_tail', []) or [])[-12:],
                    'raw_event_tail': list(run.get('raw_event_tail', []) or [])[-12:],
                    'unittest_command_seen': unittest_seen,
                    'config_path': str(config_path),
                    'detail': 'user requested abort',
                    **context_fields,
                }
            check_cancelled(cancel_token)
            tests_ok, test_detail = verify_fixture(workspace)
            stdout_tail = list(run.get('stdout', []) or [])[-12:]
            stderr_tail = list(run.get('stderr', []) or [])[-12:]
            stderr = ' | '.join(str(line) for line in stderr_tail[-8:])
            stdout = ' | '.join(str(line) for line in stdout_tail[-8:])
            detail = test_detail or stderr or stdout
            context_required = parse_context_requirement(' | '.join([detail, stderr, stdout]))
            if context_required:
                status = 'context too small'
                detail = f'Hermes requested about {context_required} tokens; {detail}'
            elif run.get('no_output_timeout'):
                status = 'hermes no output timeout'
                detail = f'no Hermes output for {HERMES_NO_OUTPUT_TIMEOUT}s'
            elif run.get('idle_output_timeout'):
                status = 'hermes idle timeout'
                detail = f'no Hermes output for {HERMES_IDLE_OUTPUT_TIMEOUT}s after initial output'
            elif run.get('timed_out'):
                status = 'hermes timed out'
            elif int(run.get('returncode', -1)) != 0:
                status = 'hermes command failed'
            elif tests_ok:
                status = 'tests passed'
            else:
                status = 'tests failed'
            sample = {
                'task': task.name,
                'command_preview': preview,
                'config_path': str(config_path),
                'ok': bool(status == 'tests passed'),
                'tests_ok': tests_ok,
                'status': status,
                'exit_code': int(run.get('returncode', -1)),
                'timed_out': bool(run.get('timed_out')),
                'no_output_timeout': bool(run.get('no_output_timeout')),
                'idle_output_timeout': bool(run.get('idle_output_timeout')),
                'aborted': bool(run.get('aborted')),
                'elapsed': float(run.get('elapsed', 0.0) or 0.0),
                'first_output': float(run.get('first_output', 0.0) or 0.0),
                'min_ram_available': int(run.get('min_ram_available', 0) or 0),
                'min_gpu_memory_free': int(run.get('min_gpu_memory_free', 0) or 0),
                'stdout_tail': stdout_tail,
                'stderr_tail': stderr_tail,
                'json_event_tail': list(run.get('json_event_tail', []) or [])[-12:],
                'raw_event_tail': list(run.get('raw_event_tail', []) or [])[-12:],
                'unittest_command_seen': unittest_seen,
                'context_required': context_required,
                'detail': concise_failure(detail, limit=500),
                **context_fields,
            }
            if not sample['ok']:
                diagnostic = (
                    f'Hermes task {task.name} {status} exit={sample["exit_code"]} '
                    f'config={config_path} detail={sample["detail"]}'
                )
                app.append_log(model.id, diagnostic)
                if stderr_tail:
                    app.append_log(model.id, 'Hermes stderr tail: ' + concise_failure(' | '.join(str(line) for line in stderr_tail), limit=700))
                if stdout_tail:
                    app.append_log(model.id, 'Hermes stdout tail: ' + concise_failure(' | '.join(str(line) for line in stdout_tail), limit=700))
                if progress:
                    emit_benchmark_event(
                        progress,
                        'benchmark_phase',
                        model,
                        'hermes',
                        message=diagnostic,
                        phase='Hermes benchmark (headless)',
                        candidate=task.name,
                    )
                    if stderr_tail:
                        emit_benchmark_event(
                            progress,
                            'benchmark_phase',
                            model,
                            'hermes',
                            message='Hermes stderr tail: ' + concise_failure(' | '.join(str(line) for line in stderr_tail), limit=700),
                            phase='Hermes benchmark (headless)',
                            candidate=task.name,
                        )
                    elif stdout_tail:
                        emit_benchmark_event(
                            progress,
                            'benchmark_phase',
                            model,
                            'hermes',
                            message='Hermes stdout tail: ' + concise_failure(' | '.join(str(line) for line in stdout_tail), limit=700),
                            phase='Hermes benchmark (headless)',
                            candidate=task.name,
                        )
            return sample


def summarize_hermes_sample_status(samples: List[Dict[str, object]]) -> str:
    if samples and all(sample.get('ok') for sample in samples):
        return 'tests passed'
    statuses = [str(sample.get('status', '') or '') for sample in samples]
    for candidate in (
        'aborted',
        'context too small',
        'hermes no output timeout',
        'hermes idle timeout',
        'hermes timed out',
        'hermes command failed',
        'tests failed',
    ):
        if candidate in statuses:
            return candidate
    return 'tests failed' if samples else 'failed'


def hermes_context_skip_record(
    app,
    preset: str,
    tier: str,
    candidate: ModelConfig,
    candidate_detail: str,
) -> Dict[str, object]:
    fields = hermes_context_record_fields(app, candidate)
    required = int(fields.get('required_context', 0) or 0)
    actual = int(fields.get('actual_ctx_per_slot', 0) or 0)
    return {
        'runtime': 'hermes',
        'preset': preset,
        'tier': tier,
        'status': 'not Hermes-ready',
        'score': 0.0,
        'seconds': 0.0,
        'passed': 0,
        'tasks': 0,
        'detail': (
            f'not Hermes-ready: needs {required} ctx/slot; '
            f'candidate has {actual}. {candidate_detail}'
        ),
        'ctx': int(getattr(candidate, 'ctx', 0) or 0),
        'ctx_per_slot': ctx_per_slot(candidate),
        'parallel': int(getattr(candidate, 'parallel', 0) or 0),
        'threads': int(getattr(candidate, 'threads', 0) or 0),
        'ngl': int(getattr(candidate, 'ngl', 0) or 0),
        'benchmarked_at': datetime.now().isoformat(timespec='seconds'),
        **fields,
    }


def benchmark_hermes_workflow(
    app,
    model: ModelConfig,
    progress: Optional[Callable[[str], None]] = None,
    cancel_token: Optional[CancelToken] = None,
) -> Tuple[bool, str]:
    check_cancelled(cancel_token)
    cli_ok, cli_msg = hermes_cli_preflight(app)
    if not cli_ok:
        return False, f'❌ Hermes preflight failed: {cli_msg}'
    if progress:
        progress(cli_msg)
    status, _detail = app.health(model)
    if status in ('READY', 'LOADING', 'STARTING') or app.get_pid(model):
        return False, '❌ Stop the model before running Hermes workflow benchmark.'

    profile = app.hardware_profile(refresh=True)
    all_candidates = opencode_candidate_models(model, profile)
    candidates: List[Tuple[str, str, ModelConfig, str]] = []
    skipped_records: List[Dict[str, object]] = []
    for preset, tier, candidate, candidate_detail in all_candidates:
        fields = hermes_context_record_fields(app, candidate)
        actual = int(fields.get('actual_ctx_per_slot', 0) or 0)
        required = int(fields.get('required_context', 0) or 0)
        experimental = bool(fields.get('experimental_context_override', False))
        if not experimental and actual < required:
            skipped_records.append(hermes_context_skip_record(app, preset, tier, candidate, candidate_detail))
            continue
        candidates.append((preset, tier, candidate, candidate_detail))
    vscode = detect_vscode_pressure()
    records: List[Dict[str, object]] = []
    results: List[Dict[str, object]] = []
    total_steps = max(1, len(skipped_records) + len(candidates) * max(1, len(HERMES_WORKFLOW_TASKS)))
    completed_steps = 0
    started_at = datetime.now().isoformat(timespec='seconds')
    run_id = f'hermes-{datetime.now().strftime("%Y%m%d-%H%M%S")}'
    current: Optional[Tuple[str, str, ModelConfig]] = None

    emit_benchmark_event(
        progress,
        'benchmark_started',
        model,
        'hermes',
        message='Hermes workflow benchmark (headless)',
        phase='preflight',
        completed=0,
        total=total_steps,
    )
    if not all_candidates:
        msg = '❌ Hermes workflow benchmark failed: no viable model candidates'
        emit_benchmark_event(progress, 'benchmark_error', model, 'hermes', message=msg, records=records)
        return False, msg
    for record in skipped_records:
        completed_steps += 1
        records.append(record)
        emit_benchmark_event(
            progress,
            'benchmark_result',
            model,
            'hermes',
            message=str(record.get('detail', 'not Hermes-ready')),
            phase='Hermes readiness',
            completed=completed_steps,
            total=total_steps,
            candidate=f'{record.get("preset", "-")}/{record.get("tier", "-")}',
            record=record,
        )
    if not candidates:
        best_available = max([int(row.get('actual_ctx_per_slot', 0) or 0) for row in skipped_records] or [0])
        required = max([int(row.get('required_context', 0) or 0) for row in skipped_records] or [int(getattr(app.hermes, 'min_context_tokens', 64000) or 64000)])
        recorded_model = clone_model_config(model)
        recorded_model.last_hermes_benchmark_score = 0.0
        recorded_model.last_hermes_benchmark_seconds = 0.0
        recorded_model.last_hermes_benchmark_profile = (
            f'failed: not Hermes-ready; needs {required} ctx/slot, best available {best_available}'
        )
        recorded_model.last_hermes_benchmark_results = records
        run = build_benchmark_run(run_id, 'hermes', 'failed', records, {}, started_at, datetime.now().isoformat(timespec='seconds'), profile.short_summary())
        run['summary'] = f'Hermes not ready: needs {required} ctx/slot; best available {best_available}'
        upsert_benchmark_run(recorded_model, run)
        app.add_or_update(recorded_model)
        msg = f'❌ Hermes workflow benchmark failed: not Hermes-ready; needs {required} ctx/slot, best available {best_available}'
        emit_benchmark_event(progress, 'benchmark_error', model, 'hermes', message=msg, phase='failed', records=records)
        return False, msg

    try:
        for attempt, (preset, tier, candidate, candidate_detail) in enumerate(candidates, start=1):
            current = (preset, tier, candidate)
            check_cancelled(cancel_token)
            if progress:
                progress(f'headless Hermes candidate {attempt}/{len(candidates)} {preset}/{tier}: {candidate_detail}')
            emit_benchmark_event(
                progress,
                'benchmark_phase',
                candidate,
                'hermes',
                message=f'headless Hermes candidate {attempt}/{len(candidates)} {preset}/{tier}',
                phase='Hermes benchmark (headless)',
                candidate=f'{preset}/{tier} ctx/slot={ctx_per_slot(candidate)}',
                completed=completed_steps,
                total=total_steps,
            )
            ok, msg = app.start(candidate)
            if not ok:
                completed_steps += len(HERMES_WORKFLOW_TASKS)
                record = {
                    'runtime': 'hermes',
                    'preset': preset,
                    'tier': tier,
                    'status': 'start failed',
                    'score': 0.0,
                    'seconds': 0.0,
                    'passed': 0,
                    'tasks': len(HERMES_WORKFLOW_TASKS),
                    'detail': concise_failure(msg, limit=500),
                    'ctx': int(getattr(candidate, 'ctx', 0) or 0),
                    'ctx_per_slot': ctx_per_slot(candidate),
                    'parallel': int(getattr(candidate, 'parallel', 0) or 0),
                    'threads': int(getattr(candidate, 'threads', 0) or 0),
                    'ngl': int(getattr(candidate, 'ngl', 0) or 0),
                    'benchmarked_at': datetime.now().isoformat(timespec='seconds'),
                    **hermes_context_record_fields(app, candidate),
                }
                records.append(record)
                emit_benchmark_event(
                    progress,
                    'benchmark_result',
                    candidate,
                    'hermes',
                    message=f'Hermes candidate {attempt}/{len(candidates)} failed to start',
                    phase='Hermes benchmark (headless)',
                    completed=completed_steps,
                    total=total_steps,
                    candidate=f'{preset}/{tier}',
                    record=record,
                )
                continue

            samples: List[Dict[str, object]] = []
            candidate_started = time.monotonic()
            try:
                ready_ok, ready_msg = app.wait_until_ready(candidate, timeout=180, cancel_token=cancel_token)
                if not ready_ok:
                    completed_steps += len(HERMES_WORKFLOW_TASKS)
                    record = {
                        'runtime': 'hermes',
                        'preset': preset,
                        'tier': tier,
                        'status': 'not ready',
                        'score': 0.0,
                        'seconds': 0.0,
                        'passed': 0,
                        'tasks': len(HERMES_WORKFLOW_TASKS),
                        'detail': concise_failure(ready_msg, limit=500),
                        'ctx': int(getattr(candidate, 'ctx', 0) or 0),
                        'ctx_per_slot': ctx_per_slot(candidate),
                        'parallel': int(getattr(candidate, 'parallel', 0) or 0),
                        'threads': int(getattr(candidate, 'threads', 0) or 0),
                        'ngl': int(getattr(candidate, 'ngl', 0) or 0),
                        'benchmarked_at': datetime.now().isoformat(timespec='seconds'),
                        **hermes_context_record_fields(app, candidate),
                    }
                    records.append(record)
                    emit_benchmark_event(
                        progress,
                        'benchmark_result',
                        candidate,
                        'hermes',
                        message=f'Hermes candidate {attempt}/{len(candidates)} not ready',
                        phase='Hermes benchmark (headless)',
                        completed=completed_steps,
                        total=total_steps,
                        candidate=f'{preset}/{tier}',
                        record=record,
                    )
                    continue
                for task in HERMES_WORKFLOW_TASKS:
                    check_cancelled(cancel_token)
                    completed_steps += 1
                    emit_benchmark_event(
                        progress,
                        'benchmark_phase',
                        candidate,
                        'hermes',
                        message=f'headless Hermes candidate {attempt}/{len(candidates)} task {task.name}',
                        phase='Hermes benchmark (headless)',
                        completed=completed_steps - 1,
                        total=total_steps,
                        candidate=f'{preset}/{tier} {task.name}',
                    )
                    sample = run_hermes_task(app, candidate, task, cancel_token=cancel_token, progress=progress)
                    samples.append(sample)
                    state = 'passed' if sample.get('ok') else str(sample.get('status', 'failed'))
                    if progress:
                        progress(f'Hermes task {task.name} {state} in {float(sample.get("elapsed", 0.0)):.1f}s')
                    if sample.get('context_required'):
                        break
                elapsed = time.monotonic() - candidate_started
                score = score_opencode_samples(samples)
                passed = sum(1 for sample in samples if sample.get('ok'))
                context_fields = hermes_context_record_fields(app, candidate)
                detail_parts = [str(sample.get('detail', '')) for sample in samples if sample.get('detail')]
                if context_fields.get('context_detail'):
                    detail_parts.append(str(context_fields.get('context_detail')))
                record = {
                    'runtime': 'hermes',
                    'preset': preset,
                    'tier': tier,
                    'status': summarize_hermes_sample_status(samples),
                    'score': score,
                    'seconds': round(elapsed, 2),
                    'passed': passed,
                    'tasks': len(samples),
                    'ctx': int(getattr(candidate, 'ctx', 0) or 0),
                    'ctx_per_slot': ctx_per_slot(candidate),
                    'parallel': int(getattr(candidate, 'parallel', 0) or 0),
                    'threads': int(getattr(candidate, 'threads', 0) or 0),
                    'ngl': int(getattr(candidate, 'ngl', 0) or 0),
                    'benchmarked_at': datetime.now().isoformat(timespec='seconds'),
                    'vscode_pressure': vscode,
                    'samples': compact_sample_details(samples),
                    'timeout_type': sample_timeout_type(samples[-1]) if samples else '',
                    'context_required': max([int(sample.get('context_required', 0) or 0) for sample in samples] or [0]),
                    'detail': concise_failure(' | '.join(detail_parts), limit=600),
                    **context_fields,
                }
                records.append(record)
                if passed > 0:
                    results.append({
                        'score': score,
                        'preset': preset,
                        'tier': tier,
                        'model': candidate,
                        'elapsed': elapsed,
                        'record': record,
                    })
                emit_benchmark_event(
                    progress,
                    'benchmark_result',
                    candidate,
                    'hermes',
                    message=f'Hermes candidate {attempt}/{len(candidates)} scored {score:.2f} ({passed}/{len(samples)} tasks)',
                    phase='Hermes benchmark (headless)',
                    completed=completed_steps,
                    total=total_steps,
                    candidate=f'{preset}/{tier}',
                    record=record,
                )
            finally:
                app.stop(candidate, managed_only=True)
                if progress:
                    progress(f'Hermes candidate {attempt}/{len(candidates)} stopped.')
                sleep_with_cancel(0.5, cancel_token)
    except CancelledError:
        if current is not None:
            preset, tier, candidate = current
            app.stop(candidate, managed_only=True)
            records.append({
                'runtime': 'hermes',
                'preset': preset,
                'tier': tier,
                'status': 'aborted',
                'score': 0.0,
                'seconds': 0.0,
                'passed': 0,
                'tasks': len(HERMES_WORKFLOW_TASKS),
                'detail': 'user requested abort',
                'ctx': int(getattr(candidate, 'ctx', 0) or 0),
                'ctx_per_slot': ctx_per_slot(candidate),
                'parallel': int(getattr(candidate, 'parallel', 0) or 0),
                'threads': int(getattr(candidate, 'threads', 0) or 0),
                'ngl': int(getattr(candidate, 'ngl', 0) or 0),
                'benchmarked_at': datetime.now().isoformat(timespec='seconds'),
                **hermes_context_record_fields(app, candidate),
            })
        recorded_model = clone_model_config(model)
        recorded_model.last_hermes_benchmark_results = records
        run = build_benchmark_run(run_id, 'hermes', 'aborted', records, {}, started_at, datetime.now().isoformat(timespec='seconds'), profile.short_summary())
        run['summary'] = 'Hermes benchmark aborted'
        upsert_benchmark_run(recorded_model, run)
        app.add_or_update(recorded_model)
        msg = '⚠ aborted; managed processes stopped'
        emit_benchmark_event(progress, 'benchmark_aborted', model, 'hermes', message=msg, phase='aborted', records=records)
        return False, msg

    recorded_model = clone_model_config(model)
    recorded_model.last_hermes_benchmark_results = records
    if not results:
        recorded_model.last_hermes_benchmark_score = 0.0
        recorded_model.last_hermes_benchmark_seconds = 0.0
        recorded_model.last_hermes_benchmark_profile = 'failed: no candidate passed a task'
        run = build_benchmark_run(run_id, 'hermes', 'failed', records, {}, started_at, datetime.now().isoformat(timespec='seconds'), profile.short_summary())
        run['summary'] = 'Hermes benchmark failed'
        upsert_benchmark_run(recorded_model, run)
        app.add_or_update(recorded_model)
        msg = '❌ Hermes workflow benchmark failed: no candidate passed a task'
        emit_benchmark_event(progress, 'benchmark_error', model, 'hermes', message=msg, phase='failed', records=records)
        return False, msg

    best = max(results, key=lambda item: float(item['score']))
    best_model = best['model']
    best_model.last_hermes_benchmark_score = round(float(best['score']), 2)
    best_model.last_hermes_benchmark_seconds = round(float(best['elapsed']), 2)
    best_model.last_hermes_benchmark_profile = (
        f'{best["preset"]}/{best["tier"]} '
        f'{float(best["score"]):.2f} score '
        f'{profile.short_summary()}'
    )
    best_model.last_hermes_benchmark_results = records
    winners = {'hermes': dict(best['record'])}
    run = build_benchmark_run(run_id, 'hermes', 'done', records, winners, started_at, datetime.now().isoformat(timespec='seconds'), profile.short_summary())
    run['summary'] = f'Hermes {float(best["score"]):.2f} score ctx={best_model.ctx} par={best_model.parallel}'
    upsert_benchmark_run(best_model, run)
    app.add_or_update(best_model)
    sync_ok, sync_msg = app.generate_hermes_config(best_model)
    msg = (
        f'✅ Hermes workflow winner: {best_model.id} {best["preset"]}/{best["tier"]} '
        f'score={float(best["score"]):.2f} ctx={best_model.ctx} parallel={best_model.parallel} '
        f'threads={best_model.threads} ngl={best_model.ngl} | {sync_msg if sync_ok else sync_msg}'
    )
    emit_benchmark_event(
        progress,
        'benchmark_done',
        best_model,
        'hermes',
        message=msg,
        phase='complete',
        completed=total_steps,
        total=total_steps,
        records=records,
    )
    return True, msg
